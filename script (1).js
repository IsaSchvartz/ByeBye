const PRODUCTOS_DATABASE = JSON.parse(JSON.stringify(eval('(' + document.currentScript.parentElement.innerHTML.split('const PRODUCTOS_DATABASE = JSON.parse')[0].split('const PRODUCTOS_DATABASE = ')[1].split(';')[0] + ')')));

/* =============================================
   ESTADO DE LA APLICACIÓN
   ============================================= */

let currentState = {
    collection: null,
    gender: null,
    category: null
};

let cart = [];

/* =============================================
   FUNCIONES AUXILIARES
   ============================================= */

function getColecciones() {
    return [...new Set(PRODUCTOS_DATABASE.map(p => p.coleccion))];
}

function getGendersByColeccion(coleccion) {
    return [...new Set(PRODUCTOS_DATABASE.filter(p => p.coleccion === coleccion).map(p => p.genero))];
}

function getCategoriesByGender(coleccion, gender) {
    return [...new Set(PRODUCTOS_DATABASE.filter(p => p.coleccion === coleccion && p.genero === gender).map(p => p.categoria))];
}

function getProductosByCategoria(coleccion, gender, categoria) {
    return PRODUCTOS_DATABASE.filter(p => p.coleccion === coleccion && p.genero === gender && p.categoria === categoria);
}

function getProductoById(id) {
    return PRODUCTOS_DATABASE.find(p => p.id === id);
}

/* =============================================
   RENDERIZAR NAVEGACIÓN
   ============================================= */

function renderNavLevel1() {
    const container = document.getElementById('navLevel1');
    const colecciones = getColecciones();
    
    container.innerHTML = '';
    
    colecciones.forEach(coleccion => {
        const btn = document.createElement('button');
        btn.className = 'nav-button active';
        btn.textContent = coleccion;
        btn.onclick = () => selectColeccion(coleccion);
        container.appendChild(btn);
    });
}

function renderNavLevel2() {
    if (!currentState.collection) return;
    
    const container = document.getElementById('navLevel2');
    const genders = getGendersByColeccion(currentState.collection);
    
    container.innerHTML = '';
    
    const backBtn = document.createElement('button');
    backBtn.className = 'nav-back';
    backBtn.textContent = '← Volver a colecciones';
    backBtn.onclick = goBackToLevel1;
    container.appendChild(backBtn);
    
    const separator = document.createElement('span');
    separator.className = 'nav-separator';
    separator.textContent = '/';
    container.appendChild(separator);
    
    genders.forEach(gender => {
        const btn = document.createElement('button');
        btn.className = 'nav-button' + (currentState.gender === gender ? ' active' : '');
        btn.textContent = gender;
        btn.onclick = () => selectGender(gender);
        container.appendChild(btn);
    });
    
    document.getElementById('navLevel2').style.display = 'flex';
}

function renderNavLevel3() {
    if (!currentState.collection || !currentState.gender) return;
    
    const container = document.getElementById('navLevel3');
    const categories = getCategoriesByGender(currentState.collection, currentState.gender);
    
    container.innerHTML = '';
    
    const backBtn = document.createElement('button');
    backBtn.className = 'nav-back';
    backBtn.textContent = '← Volver a género';
    backBtn.onclick = goBackToLevel2;
    container.appendChild(backBtn);
    
    const separator = document.createElement('span');
    separator.className = 'nav-separator';
    separator.textContent = '/';
    container.appendChild(separator);
    
    categories.forEach(category => {
        const btn = document.createElement('button');
        btn.className = 'nav-button' + (currentState.category === category ? ' active' : '');
        btn.textContent = category;
        btn.onclick = () => selectCategory(category);
        container.appendChild(btn);
    });
    
    document.getElementById('navLevel3').style.display = 'flex';
}

/* =============================================
   HANDLERS DE NAVEGACIÓN
   ============================================= */

function selectColeccion(coleccion) {
    currentState.collection = coleccion;
    currentState.gender = null;
    currentState.category = null;
    
    renderNavLevel2();
    updateBreadcrumb();
    renderProducts([]);
}

function selectGender(gender) {
    currentState.gender = gender;
    currentState.category = null;
    
    renderNavLevel3();
    updateBreadcrumb();
    renderProducts([]);
}

function selectCategory(category) {
    currentState.category = category;
    
    const productos = getProductosByCategoria(currentState.collection, currentState.gender, category);
    renderProducts(productos);
    updateBreadcrumb();
}

function goBackToLevel1() {
    currentState.collection = null;
    currentState.gender = null;
    currentState.category = null;
    
    document.getElementById('navLevel2').style.display = 'none';
    document.getElementById('navLevel3').style.display = 'none';
    updateBreadcrumb();
    renderProducts([]);
}

function goBackToLevel2() {
    currentState.category = null;
    
    document.getElementById('navLevel3').style.display = 'none';
    renderNavLevel3();
    updateBreadcrumb();
    renderProducts([]);
}

/* =============================================
   RENDERIZAR PRODUCTOS
   ============================================= */

function renderProducts(productos) {
    const container = document.getElementById('productsContainer');
    
    if (productos.length === 0) {
        container.innerHTML = '<p style="grid-column: 1/-1; text-align: center; padding: 3rem; color: #999;">Selecciona una colección y categoría para ver productos</p>';
        return;
    }
    
    container.innerHTML = '';
    
    productos.forEach((producto, idx) => {
        const card = document.createElement('div');
        card.className = 'product-card';
        card.style.setProperty('--delay', idx);
        
        const imageSrc = producto.imagenes && producto.imagenes.length > 0 ? producto.imagenes[0] : 'data:image/svg+xml,%3Csvg xmlns=%22http://www.w3.org/2000/svg%22 viewBox=%220 0 400 500%22%3E%3Crect fill=%22%23f0f0f0%22 width=%22400%22 height=%22500%22/%3E%3C/svg%3E';
        
        card.innerHTML = `
            <div class="product-image">
                <img src="${imageSrc}" alt="${producto.nombre}" loading="lazy">
            </div>
            <div class="product-info">
                <h3>${producto.nombre}</h3>
                <p class="product-category">${producto.categoria}</p>
            </div>
        `;
        
        card.onclick = () => openProductModal(producto);
        container.appendChild(card);
    });
}

/* =============================================
   MODAL DE PRODUCTO
   ============================================= */

function openProductModal(producto) {
    const modal = document.getElementById('productModal');
    const overlay = document.getElementById('overlay');
    
    document.getElementById('modalName').textContent = producto.nombre;
    document.getElementById('modalDescription').textContent = producto.descripcion;
    document.getElementById('modalCollection').textContent = producto.coleccion;
    document.getElementById('modalCategory').textContent = producto.categoria;
    
    // Imagen principal
    if (producto.imagenes && producto.imagenes.length > 0) {
        document.getElementById('mainImage').src = producto.imagenes[0];
    }
    
    // Miniaturas
    const thumbsContainer = document.getElementById('galleryThumbs');
    thumbsContainer.innerHTML = '';
    
    if (producto.imagenes && producto.imagenes.length > 1) {
        producto.imagenes.forEach((img, idx) => {
            const thumb = document.createElement('div');
            thumb.className = 'thumbnail' + (idx === 0 ? ' active' : '');
            thumb.innerHTML = `<img src="${img}" alt="Foto ${idx + 1}" loading="lazy">`;
            thumb.onclick = () => {
                document.getElementById('mainImage').src = img;
                document.querySelectorAll('.thumbnail').forEach(t => t.classList.remove('active'));
                thumb.classList.add('active');
            };
            thumbsContainer.appendChild(thumb);
        });
    }
    
    // Botón agregar
    document.getElementById('addToSelectionBtn').onclick = () => {
        addToCart(producto);
        closeProductModal();
    };
    
    modal.classList.add('active');
    overlay.classList.add('active');
}

function closeProductModal() {
    const modal = document.getElementById('productModal');
    const overlay = document.getElementById('overlay');
    
    modal.classList.remove('active');
    overlay.classList.remove('active');
}

/* =============================================
   CARRITO
   ============================================= */

function addToCart(producto) {
    cart.push({
        id: `${producto.id}-${Date.now()}`,
        nombre: producto.nombre,
        categoria: producto.categoria,
        genero: producto.genero,
        imagen: producto.imagenes && producto.imagenes.length > 0 ? producto.imagenes[0] : null
    });
    
    updateCartUI();
    showNotification(`${producto.nombre} agregado a tu selección`);
}

function removeFromCart(itemId) {
    cart = cart.filter(item => item.id !== itemId);
    updateCartUI();
}

function updateCartUI() {
    const cartCount = document.getElementById('cartCount');
    const cartItems = document.getElementById('cartItems');
    const emptyCart = document.getElementById('emptyCart');
    const downloadBtn = document.getElementById('downloadPdfBtn');
    
    cartCount.textContent = cart.length;
    
    if (cart.length === 0) {
        emptyCart.style.display = 'flex';
        cartItems.innerHTML = '';
        downloadBtn.style.display = 'none';
    } else {
        emptyCart.style.display = 'none';
        downloadBtn.style.display = 'block';
        
        cartItems.innerHTML = '';
        cart.forEach(item => {
            const cartItem = document.createElement('div');
            cartItem.className = 'cart-item';
            cartItem.innerHTML = `
                <div class="cart-item-content">
                    <div class="cart-item-name">${item.nombre}</div>
                    <div class="cart-item-details">${item.categoria} • ${item.genero}</div>
                </div>
                <button class="cart-item-remove" onclick="removeFromCart('${item.id}')">×</button>
            `;
            cartItems.appendChild(cartItem);
        });
    }
}

function toggleCartSidebar() {
    const sidebar = document.getElementById('cartSidebar');
    const overlay = document.getElementById('overlay');
    
    sidebar.classList.toggle('active');
    if (cart.length > 0) {
        overlay.classList.toggle('active');
    }
}

/* =============================================
   BREADCRUMB
   ============================================= */

function updateBreadcrumb() {
    const breadcrumb = document.getElementById('breadcrumb');
    const parts = ['Inicio'];
    
    if (currentState.collection) {
        parts.push(currentState.collection);
    }
    if (currentState.gender) {
        parts.push(currentState.gender);
    }
    if (currentState.category) {
        parts.push(currentState.category);
    }
    
    breadcrumb.innerHTML = parts.map(part => `<span>${part}</span>`).join(' / ');
}

