function sendWhatsAppMessage() {
    const message = `Hola, quiero cotizar la siguiente selección de productos del Catálogo Verano 2027:\n\n${cart.map(item => `• ${item.nombre} (${item.categoria})`).join('\n')}\n\nTotal de productos: ${cart.length}\n\nPuede enviarme información y disponibilidad.`;
    
    const encodedMessage = encodeURIComponent(message);
    window.open(`https://wa.me/?text=${encodedMessage}`, '_blank');
    
    showNotification('Abriendo WhatsApp...');
}

